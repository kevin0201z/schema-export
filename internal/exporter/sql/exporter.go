package sql

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/schema-export/schema-export/internal/exporter"
	"github.com/schema-export/schema-export/internal/model"
)

type Exporter struct {
	dialect Dialect
}

func NewExporter() *Exporter {
	return &Exporter{
		dialect: &GenericDialect{},
	}
}

func NewExporterWithDialect(dbType string) *Exporter {
	return &Exporter{
		dialect: GetDialect(dbType),
	}
}

func (e *Exporter) Export(tables []model.Table, views []model.View, procedures []model.Procedure, functions []model.Function, triggers []model.Trigger, sequences []model.Sequence, options exporter.ExportOptions) error {
	if options.DbType != "" {
		e.dialect = GetDialect(options.DbType)
	}

	if options.SplitFiles {
		return e.exportSplitFiles(tables, views, procedures, functions, triggers, sequences, options)
	}
	return e.exportSingleFile(tables, views, procedures, functions, triggers, sequences, options)
}

func (e *Exporter) exportSingleFile(tables []model.Table, views []model.View, procedures []model.Procedure, functions []model.Function, triggers []model.Trigger, sequences []model.Sequence, options exporter.ExportOptions) error {
	outputPath := filepath.Join(options.OutputDir, options.FileName)
	if outputPath == "" || outputPath == options.OutputDir {
		outputPath = filepath.Join(options.OutputDir, "schema.sql")
	}

	info, err := os.Stat(outputPath)
	if err == nil && info.IsDir() {
		outputPath = filepath.Join(outputPath, "schema.sql")
	}

	if err := os.MkdirAll(filepath.Dir(outputPath), 0755); err != nil {
		return fmt.Errorf("failed to create output directory: %w", err)
	}

	file, err := os.Create(outputPath)
	if err != nil {
		return fmt.Errorf("failed to create file: %w", err)
	}
	defer file.Close()

	fmt.Fprintln(file, "-- ========================================================")
	fmt.Fprintln(file, "-- Database Schema DDL")
	fmt.Fprintln(file, "-- Generated by schema-export tool")
	fmt.Fprintln(file, "-- Dialect:", e.dialect.GetName())
	fmt.Fprintln(file, "-- ========================================================")
	fmt.Fprintln(file, "")
	fmt.Fprintf(file, "-- Total Tables: %d\n", len(tables))
	if options.IncludeViews {
		fmt.Fprintf(file, "-- Total Views: %d\n", len(views))
	}
	if options.IncludeProcedures {
		fmt.Fprintf(file, "-- Total Procedures: %d\n", len(procedures))
	}
	if options.IncludeFunctions {
		fmt.Fprintf(file, "-- Total Functions: %d\n", len(functions))
	}
	if options.IncludeTriggers {
		fmt.Fprintf(file, "-- Total Triggers: %d\n", len(triggers))
	}
	if options.IncludeSequences {
		fmt.Fprintf(file, "-- Total Sequences: %d\n", len(sequences))
	}
	fmt.Fprintln(file, "")

	for _, table := range tables {
		if err := e.writeTableDDL(file, &table); err != nil {
			return err
		}
		fmt.Fprintln(file, "")
	}

	if options.IncludeViews && len(views) > 0 {
		fmt.Fprintln(file, "-- ========================================================")
		fmt.Fprintln(file, "-- Views")
		fmt.Fprintln(file, "-- ========================================================")
		fmt.Fprintln(file, "")

		for _, view := range views {
			if err := e.writeViewDDL(file, &view); err != nil {
				return err
			}
			fmt.Fprintln(file, "")
		}
	}

	if options.IncludeProcedures && len(procedures) > 0 {
		fmt.Fprintln(file, "-- ========================================================")
		fmt.Fprintln(file, "-- Procedures")
		fmt.Fprintln(file, "-- ========================================================")
		fmt.Fprintln(file, "")

		for _, proc := range procedures {
			if err := e.writeProcedureDDL(file, &proc); err != nil {
				return err
			}
			fmt.Fprintln(file, "")
		}
	}

	if options.IncludeFunctions && len(functions) > 0 {
		fmt.Fprintln(file, "-- ========================================================")
		fmt.Fprintln(file, "-- Functions")
		fmt.Fprintln(file, "-- ========================================================")
		fmt.Fprintln(file, "")

		for _, fn := range functions {
			if err := e.writeFunctionDDL(file, &fn); err != nil {
				return err
			}
			fmt.Fprintln(file, "")
		}
	}

	if options.IncludeTriggers && len(triggers) > 0 {
		fmt.Fprintln(file, "-- ========================================================")
		fmt.Fprintln(file, "-- Triggers")
		fmt.Fprintln(file, "-- ========================================================")
		fmt.Fprintln(file, "")

		for _, tr := range triggers {
			if err := e.writeTriggerDDL(file, &tr); err != nil {
				return err
			}
			fmt.Fprintln(file, "")
		}
	}

	if options.IncludeSequences && len(sequences) > 0 {
		fmt.Fprintln(file, "-- ========================================================")
		fmt.Fprintln(file, "-- Sequences")
		fmt.Fprintln(file, "-- ========================================================")
		fmt.Fprintln(file, "")

		for _, seq := range sequences {
			if err := e.writeSequenceDDL(file, &seq); err != nil {
				return err
			}
			fmt.Fprintln(file, "")
		}
	}

	return nil
}

func (e *Exporter) exportSplitFiles(tables []model.Table, views []model.View, procedures []model.Procedure, functions []model.Function, triggers []model.Trigger, sequences []model.Sequence, options exporter.ExportOptions) error {
	sqlDir := filepath.Join(options.OutputDir, "sql")
	if err := os.MkdirAll(sqlDir, 0755); err != nil {
		return fmt.Errorf("failed to create sql directory: %w", err)
	}

	for _, table := range tables {
		outputPath := filepath.Join(sqlDir, table.Name+".sql")
		file, err := os.Create(outputPath)
		if err != nil {
			return fmt.Errorf("failed to create file %s: %w", outputPath, err)
		}

		fmt.Fprintf(file, "-- ========================================================\n")
		fmt.Fprintf(file, "-- Table: %s\n", table.Name)
		if table.Comment != "" {
			fmt.Fprintf(file, "-- Description: %s\n", table.Comment)
		}
		fmt.Fprintf(file, "-- Dialect: %s\n", e.dialect.GetName())
		fmt.Fprintf(file, "-- ========================================================\n")
		fmt.Fprintln(file, "")

		if err := e.writeTableDDL(file, &table); err != nil {
			file.Close()
			return err
		}
		file.Close()
	}

	if options.IncludeViews {
		for _, view := range views {
			outputPath := filepath.Join(sqlDir, view.Name+"_view.sql")
			file, err := os.Create(outputPath)
			if err != nil {
				return fmt.Errorf("failed to create file %s: %w", outputPath, err)
			}

			fmt.Fprintf(file, "-- ========================================================\n")
			fmt.Fprintf(file, "-- View: %s\n", view.Name)
			if view.Comment != "" {
				fmt.Fprintf(file, "-- Description: %s\n", view.Comment)
			}
			fmt.Fprintf(file, "-- Dialect: %s\n", e.dialect.GetName())
			fmt.Fprintf(file, "-- ========================================================\n")
			fmt.Fprintln(file, "")

			if err := e.writeViewDDL(file, &view); err != nil {
				file.Close()
				return err
			}
			file.Close()
		}
	}

	if options.IncludeProcedures {
		for _, proc := range procedures {
			outputPath := filepath.Join(sqlDir, proc.Name+"_procedure.sql")
			file, err := os.Create(outputPath)
			if err != nil {
				return fmt.Errorf("failed to create file %s: %w", outputPath, err)
			}
			fmt.Fprintf(file, "-- ========================================================\n")
			fmt.Fprintf(file, "-- Procedure: %s\n", proc.Name)
			if proc.Comment != "" {
				fmt.Fprintf(file, "-- Description: %s\n", proc.Comment)
			}
			fmt.Fprintf(file, "-- ========================================================\n")
			fmt.Fprintln(file, "")
			if err := e.writeProcedureDDL(file, &proc); err != nil {
				file.Close()
				return err
			}
			file.Close()
		}
	}

	if options.IncludeFunctions {
		for _, fn := range functions {
			outputPath := filepath.Join(sqlDir, fn.Name+"_function.sql")
			file, err := os.Create(outputPath)
			if err != nil {
				return fmt.Errorf("failed to create file %s: %w", outputPath, err)
			}
			fmt.Fprintf(file, "-- ========================================================\n")
			fmt.Fprintf(file, "-- Function: %s\n", fn.Name)
			if fn.Comment != "" {
				fmt.Fprintf(file, "-- Description: %s\n", fn.Comment)
			}
			fmt.Fprintf(file, "-- ========================================================\n")
			fmt.Fprintln(file, "")
			if err := e.writeFunctionDDL(file, &fn); err != nil {
				file.Close()
				return err
			}
			file.Close()
		}
	}

	if options.IncludeTriggers {
		for _, tr := range triggers {
			outputPath := filepath.Join(sqlDir, tr.Name+"_trigger.sql")
			file, err := os.Create(outputPath)
			if err != nil {
				return fmt.Errorf("failed to create file %s: %w", outputPath, err)
			}
			fmt.Fprintf(file, "-- ========================================================\n")
			fmt.Fprintf(file, "-- Trigger: %s\n", tr.Name)
			fmt.Fprintf(file, "-- Table: %s\n", tr.TableName)
			fmt.Fprintf(file, "-- ========================================================\n")
			fmt.Fprintln(file, "")
			if err := e.writeTriggerDDL(file, &tr); err != nil {
				file.Close()
				return err
			}
			file.Close()
		}
	}

	if options.IncludeSequences {
		for _, seq := range sequences {
			outputPath := filepath.Join(sqlDir, seq.Name+"_sequence.sql")
			file, err := os.Create(outputPath)
			if err != nil {
				return fmt.Errorf("failed to create file %s: %w", outputPath, err)
			}
			fmt.Fprintf(file, "-- ========================================================\n")
			fmt.Fprintf(file, "-- Sequence: %s\n", seq.Name)
			fmt.Fprintf(file, "-- ========================================================\n")
			fmt.Fprintln(file, "")
			if err := e.writeSequenceDDL(file, &seq); err != nil {
				file.Close()
				return err
			}
			file.Close()
		}
	}

	return nil
}

func (e *Exporter) writeTableDDL(file *os.File, table *model.Table) error {
	fmt.Fprintf(file, "-- --------------------------------------------------------\n")
	fmt.Fprintf(file, "-- Table: %s\n", table.Name)
	fmt.Fprintln(file, "-- --------------------------------------------------------")
	if table.Comment != "" {
		fmt.Fprintf(file, "-- Description: %s\n", table.Comment)
	}
	fmt.Fprintf(file, "-- Type: %s\n", table.Type)
	fmt.Fprintf(file, "-- Columns: %d\n", len(table.Columns))
	fmt.Fprintf(file, "-- Indexes: %d\n", len(table.Indexes))
	fmt.Fprintf(file, "-- Foreign Keys: %d\n", len(table.ForeignKeys))
	fmt.Fprintf(file, "-- Check Constraints: %d\n", len(table.CheckConstraints))
	fmt.Fprintln(file, "")

	fmt.Fprintf(file, "CREATE TABLE %s (\n", e.dialect.QuoteIdentifier(table.Name))

	for i, col := range table.Columns {
		colDef := e.dialect.GetColumnDefinition(&col)
		if i < len(table.Columns)-1 || len(table.CheckConstraints) > 0 {
			fmt.Fprintf(file, "    %s,\n", colDef)
		} else {
			fmt.Fprintf(file, "    %s\n", colDef)
		}
	}

	for i, cc := range table.CheckConstraints {
		if i < len(table.CheckConstraints)-1 {
			fmt.Fprintf(file, "    %s,\n", e.dialect.GetCheckConstraint(&cc))
		} else {
			fmt.Fprintf(file, "    %s\n", e.dialect.GetCheckConstraint(&cc))
		}
	}

	fmt.Fprintln(file, ");")

	if table.Comment != "" {
		commentSQL := e.dialect.GetTableCommentSQL(table.Name, table.Comment)
		if commentSQL != "" {
			fmt.Fprintln(file, "")
			fmt.Fprintln(file, commentSQL)
		}
	}

	if !e.dialect.SupportsInlineComment() {
		for _, col := range table.Columns {
			if col.Comment != "" {
				commentSQL := e.dialect.GetColumnCommentSQL(table.Name, &col)
				if commentSQL != "" {
					fmt.Fprintln(file, commentSQL)
				}
			}
		}
	}

	if len(table.Indexes) > 0 {
		fmt.Fprintln(file, "")
		fmt.Fprintln(file, "-- --------------------------------------------------------")
		fmt.Fprintf(file, "-- Indexes for %s\n", table.Name)
		fmt.Fprintln(file, "-- --------------------------------------------------------")
		for _, idx := range table.Indexes {
			if idx.IsPrimary {
				fmt.Fprintf(file, "-- Primary Key: %s on (%s)\n", idx.Name, strings.Join(idx.Columns, ", "))
			} else {
				uniqueStr := ""
				if idx.IsUnique {
					uniqueStr = "UNIQUE "
				}
				fmt.Fprintf(file, "CREATE %sINDEX %s ON %s (%s);\n",
					uniqueStr,
					e.dialect.QuoteIdentifier(idx.Name),
					e.dialect.QuoteIdentifier(table.Name),
					e.quoteColumns(idx.Columns))
			}
		}
	}

	if len(table.ForeignKeys) > 0 {
		fmt.Fprintln(file, "")
		fmt.Fprintln(file, "-- --------------------------------------------------------")
		fmt.Fprintf(file, "-- Foreign Keys for %s\n", table.Name)
		fmt.Fprintln(file, "-- --------------------------------------------------------")
		for _, fk := range table.ForeignKeys {
			fmt.Fprintf(file, "-- Relationship: %s.%s -> %s.%s\n",
				table.Name, fk.Column, fk.RefTable, fk.RefColumn)
			fmt.Fprintf(file, "ALTER TABLE %s ADD CONSTRAINT %s FOREIGN KEY (%s) REFERENCES %s(%s)",
				e.dialect.QuoteIdentifier(table.Name),
				e.dialect.QuoteIdentifier(fk.Name),
				e.dialect.QuoteIdentifier(fk.Column),
				e.dialect.QuoteIdentifier(fk.RefTable),
				e.dialect.QuoteIdentifier(fk.RefColumn))
			if fk.OnDelete != "" {
				fmt.Fprintf(file, " ON DELETE %s", fk.OnDelete)
			}
			fmt.Fprintln(file, ";")
		}
	}

	return nil
}

func (e *Exporter) writeViewDDL(file *os.File, view *model.View) error {
	fmt.Fprintf(file, "-- --------------------------------------------------------\n")
	fmt.Fprintf(file, "-- View: %s\n", view.Name)
	fmt.Fprintln(file, "-- --------------------------------------------------------")
	if view.Comment != "" {
		fmt.Fprintf(file, "-- Description: %s\n", view.Comment)
	}
	fmt.Fprintln(file, "")

	fmt.Fprintf(file, "CREATE OR REPLACE VIEW %s AS\n", e.dialect.QuoteIdentifier(view.Name))
	fmt.Fprintf(file, "%s;\n", view.Definition)

	if view.Comment != "" {
		commentSQL := e.dialect.GetViewCommentSQL(view.Name, view.Comment)
		if commentSQL != "" {
			fmt.Fprintln(file, "")
			fmt.Fprintln(file, commentSQL)
		}
	}

	return nil
}

func (e *Exporter) writeProcedureDDL(file *os.File, proc *model.Procedure) error {
	fmt.Fprintf(file, "-- --------------------------------------------------------\n")
	fmt.Fprintf(file, "-- Procedure: %s\n", proc.Name)
	fmt.Fprintln(file, "-- --------------------------------------------------------")
	if proc.Comment != "" {
		fmt.Fprintf(file, "-- Description: %s\n", proc.Comment)
	}
	fmt.Fprintln(file, "")

	if proc.Definition != "" {
		fmt.Fprintln(file, proc.Definition)
		if !strings.HasSuffix(strings.TrimSpace(proc.Definition), ";") {
			fmt.Fprintln(file, ";")
		}
	}
	return nil
}

func (e *Exporter) writeFunctionDDL(file *os.File, fn *model.Function) error {
	fmt.Fprintf(file, "-- --------------------------------------------------------\n")
	fmt.Fprintf(file, "-- Function: %s\n", fn.Name)
	fmt.Fprintln(file, "-- --------------------------------------------------------")
	if fn.Comment != "" {
		fmt.Fprintf(file, "-- Description: %s\n", fn.Comment)
	}
	if fn.ReturnType != "" {
		fmt.Fprintf(file, "-- Return Type: %s\n", fn.ReturnType)
	}
	fmt.Fprintln(file, "")

	if fn.Definition != "" {
		fmt.Fprintln(file, fn.Definition)
		if !strings.HasSuffix(strings.TrimSpace(fn.Definition), ";") {
			fmt.Fprintln(file, ";")
		}
	}
	return nil
}

func (e *Exporter) writeTriggerDDL(file *os.File, tr *model.Trigger) error {
	fmt.Fprintf(file, "-- --------------------------------------------------------\n")
	fmt.Fprintf(file, "-- Trigger: %s\n", tr.Name)
	fmt.Fprintln(file, "-- --------------------------------------------------------")
	fmt.Fprintf(file, "-- Table: %s\n", tr.TableName)
	if tr.Event != "" {
		fmt.Fprintf(file, "-- Event: %s\n", tr.Event)
	}
	if tr.Timing != "" {
		fmt.Fprintf(file, "-- Timing: %s\n", tr.Timing)
	}
	fmt.Fprintf(file, "-- Status: %s\n", tr.Status)
	fmt.Fprintln(file, "")

	if tr.Definition != "" {
		fmt.Fprintln(file, tr.Definition)
		if !strings.HasSuffix(strings.TrimSpace(tr.Definition), ";") {
			fmt.Fprintln(file, ";")
		}
	}
	return nil
}

func (e *Exporter) writeSequenceDDL(file *os.File, seq *model.Sequence) error {
	fmt.Fprintf(file, "-- --------------------------------------------------------\n")
	fmt.Fprintf(file, "-- Sequence: %s\n", seq.Name)
	fmt.Fprintln(file, "-- --------------------------------------------------------")
	fmt.Fprintln(file, "")

	fmt.Fprintf(file, "CREATE SEQUENCE %s\n", e.dialect.QuoteIdentifier(seq.Name))
	if seq.MinValue != 0 {
		fmt.Fprintf(file, "    MINVALUE %d\n", seq.MinValue)
	}
	if seq.MaxValue != 0 {
		fmt.Fprintf(file, "    MAXVALUE %d\n", seq.MaxValue)
	}
	if seq.IncrementBy != 0 {
		fmt.Fprintf(file, "    INCREMENT BY %d\n", seq.IncrementBy)
	}
	if seq.Cycle {
		fmt.Fprintln(file, "    CYCLE")
	}
	if seq.CacheSize > 0 {
		fmt.Fprintf(file, "    CACHE %d\n", seq.CacheSize)
	}
	fmt.Fprintln(file, ";")
	return nil
}

func (e *Exporter) quoteColumns(columns []string) string {
	quoted := make([]string, len(columns))
	for i, col := range columns {
		quoted[i] = e.dialect.QuoteIdentifier(col)
	}
	return strings.Join(quoted, ", ")
}

func (e *Exporter) GetName() string {
	return "sql"
}

func (e *Exporter) GetExtension() string {
	return ".sql"
}

type Factory struct{}

func (f *Factory) Create() (exporter.Exporter, error) {
	return NewExporter(), nil
}

func (f *Factory) GetType() string {
	return "sql"
}

func init() {
	exporter.Register("sql", &Factory{})
}
